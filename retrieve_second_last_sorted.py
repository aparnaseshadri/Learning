"""
Initialize List:

Define a list with some elements.
Sort List:

Sort the list in ascending order.
Get the 2nd to Last Element:

Access the element at the position -2 (second to last) in the sorted list.
Print the Result:

Output the 2nd to last element.
"""
# Step 1: Initialize List
my_list = [5, 2, 9, 1, 5, 6]

# Step 2: Sort List
my_list.sort()

# Step 3: Get the 2nd to Last Element
second_to_last_element = my_list[-2]

# Step 4: Print the Result
print("The 2nd to last element is:", second_to_last_element)
